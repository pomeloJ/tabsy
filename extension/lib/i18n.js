let _locale = 'en';
let _dict = locales.en;

/**
 * Translate a key, with optional interpolation: t('hello', { name: 'World' })
 */
export function t(key, params) {
  let str = _dict[key] ?? locales.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replaceAll(`{${k}}`, v);
    }
  }
  return str;
}

export function getLocale() {
  return _locale;
}

export async function setLocale(locale) {
  if (!locales[locale]) return;
  _locale = locale;
  _dict = locales[locale];
  await chrome.storage.local.set({ tabsyLocale: locale });
}

export async function initLocale() {
  const { tabsyLocale } = await chrome.storage.local.get('tabsyLocale');
  _locale = tabsyLocale && locales[tabsyLocale] ? tabsyLocale : 'en';
  _dict = locales[_locale];
}

export function getAvailableLocales() {
  return [
    { code: 'en', name: 'English' },
    { code: 'zh-TW', name: '繁體中文' }
  ];
}
